			<h2>Popolarità</h2>
				<p>Tuohou, grazie soprattutto ad internet, si è ampliamente diffuso in tutto il mondo, soprattutto grazie a piattaforme social come <span xml:lang="en">Youtube</span>, <span xml:lang="en">4chan</span> e <span xml:lang="en">forum</span>.<br/>
				Di conseguenza sono nati molte opere derivate o tormentoni a riguardo, spesso chiamati anche "<strong xml:lang="en">meme</strong>".</p>
			<h3 xml:lang="en">Easy mode?</h3>
			<p>Una delle cose che caratterizzano Touhou è la sua <strong>difficoltà</strong>, infatti il gioco già al livello di difficoltà più basso (<span xml:lang="en">easy mode</span>) rappresenta una sfida per molti giocatori, a differenza di molti altri videogiochi dove la modalità con difficoltà più bassa è solitamente pensata ad un pubblico molto giovane od inesperto.</p>
<img src="images/easymode.jpg" alt="Comparse del gioco che deridono il lettore perchè gioca a difficoltà easy"/>
<p>Su questo tema sono state create molte immagini, la più diffusa a riguardo vede due comparse del videogioco che deridono il lettore perchè gioca a difficoltà facile, affermando che solamente i bambini ci giocano.</p>
<img src="images/rain.jpg" alt="Schermate del gioco, con una moltitudine di proiettili"/>
<p>Analogamente la difficoltà più elevata, denominata <em xml:lang="en">nightmare</em>, "incubo", viene considerata difficile tanto quanto "riuscire a correre sotto la piggia senza bagnarsi".</p>

			<h3><span xml:lang="en">U.N. Owen Was Her</span> e <span xml:lang="en">Night of Nights</span></h3>
			<p>Di particolare successo sono le <strong>musiche</strong>, o <acronym title="Original SoundTrack">OST</acronym>, presenti nel gioco, tra le quali le più diffuse sono certamente <strong xml:lang="en">U.N. Owen Was Her</strong> e <strong xml:lang="en">Night of Nights</strong>, entrambe appartenenti al sesto gioco della serie, <em xml:lang="en">Embodiment of Scarlet Devil</em>.<br/></p>
<p><strong xml:lang="en">U.N. Owen Was Her</strong> è la colonna sonora del boss extra del gioco, <em xml:lang="en">Flandre Scarlet</em>, accessibile solamente riuscendo a finire il gioco senza mai finire il primo gruppo di vite a disposizione.<br/>
Al momento la versione originale ha oltre 10 milioni visualizzazioni su <span xml:lang="en">Youtube</span>, dove solitamente viene accompagnato da immagini o <span xml:lang="en">slideshow</span> riferite a <span xml:lang="en">Flandre</span> Scarlet.<br/>
<img src="images/UNOwenWasHere.jpg" alt="Immagine con i simboli di Flandre"/>
Oltre alla versione originale sono presenti <em>moltissimi altri arrangiamenti</em> dello stesso brano, suonato con pianoforti, violini, da orchestre, in genere <span xml:lang="en">metal</span> o con del testo originale in lingua giapponese e pure tradotto in svariate lingue e in molteplici versioni, tra le quali l'inglese e l'italiano.<br/>
Nel 2008 apparve su NicoNicoVideo, e poi caricato anche su <span xml:lang="en">Youtube</span>, "<strong xml:lang="en">Ronald McDonald insanity</strong>", una nuova <span xml:lang="en">cover</span> della traccia che presenta la melodia ed il filmato composti attraverso un <span xml:lang="en">editing</span> ad una pubblicità di McDonals. Attualmente questo video su <span xml:lang="en">YouTube</span> ha oltre 11 milioni di visualizzazioni.</p>
<p><strong xml:lang="en">Night of Nights</strong> è in realtà un anch'esso un remix di un altro brano, <strong xml:lang="en">Flowering Night</strong>, anch'esso nel sesto capitolo della serie, che ne modifica le tonalità. <em xml:lang="en">Flowering Night</em> viene suonata durante lo scontro il boss di fine livello <em>Sakuya Izayoi</em>, incontrato verso la fine del videogioco. Questo brano presenta meno arrangiamenti rispetto a <span xml:lang="en">U.N. Owen Was Her</span>, ma riesce comunque ad avere circa 6 milioni di visualizzazioni su <span xml:lang="en">YouTube</span></p>

			<h3 xml:lang="en">Bad Apple</h3>
			<p>Musica originaria da un gioco arcade di Touhou, a differenza delle altre canzoni, originarie dalla serie ufficiali, <strong>presenta un testo</strong> e non unicamente una melodia.<br/>
Diventò popolare nel 2009, quando venne diffuso su NicoNicoVideo e successivamente su <span xml:lang="en">Youtube</span> in abbinamento a un filmato realizzato da un <span xml:lang="en">fan</span>.</p>
<img src="images/badapple.jpg" alt="Screenshot del filmato, con sagoma di Flandre"/>
<p>Il filmato è completamente realizzato utilizzando unicamente il bianco ed il nero e mostra svatiare sagome dei personaggi della serie, iniziando dalle due protagoniste, <em>Reimu e Marisa</em>, la seconda in procinto ad arrivare alla magione esplorata durante il gioco.
Successivamente si mostrano personaggi principali del sesto videogioco, in ordine di apparizione: <em>Patchouli, Remilia, Sakuya, <span xml:lang="en">Flandre</span></em>, e poi prosegue mostrando in ordine sparso altri personaggi caratteristici del franchise.</p>
<p>Questo filmato ha raggiunto le 24 milioni di visualizzazioni su NicoNicoVideo e 23 milioni sommando i tre caricamenti da parte di account diversi su <span xml:lang="en">Youtube</span>.<br/>Di particolare successo è anche la <a href="https://www.youtube.com/watch?v=240Vq6tIxio" title="video su Youtube di Bad Apple realizzato in stop motion"><strong>versione di <span xml:lang="en">Bad Apple</span> realizzata in <span xml:lang="en">stop motion</span></strong></a>, visibile su <span xml:lang="en">YouTube</span>, realizzato stampando i <span xml:lang="en">frame</span> del filmato, utilizzando ben 6565 pezzettini di carta stampati, che ha superato le 4 milioni di visualizzazioni complessive.</p>
			<h3><span xml:lang="en">9 ball</span> ⑨ - baka</h3>
			<p><em xml:lang="en">Nine ball</em>, letteralmente "<strong>palla numero 9</strong>", a volte indicato con <strong>9</strong>, <strong>(9)</strong> o <strong>⑨</strong>, è un simbolo che viene utilizzato per indicare il personaggio di <strong>Cirno</strong>, ha avuto origine nel manuale del nono gioco della serie, dove ZUN, in uno <span xml:lang="en">screenshot</span>, la ha etichettata con un numero 9 cerchiato che nella leggenda annessa corrisponde a <em><span xml:lang="ja">ばか</span>, baka</em>, cioè "idiota".<br/>
<img src="images/Cirno9_Windows.jpg" alt="Cirno quasi in lacrime perchè Windows ha saltato il numero 9"/>
			Questo epiteto le è stato assegnato perchè non si rivela particolamente intelligente durante i dialoghi con le protagoniste ed alcune sue <em xml:lang="en">spellcard</em> hanno alcuni punti ciechi enormi, che permettono al giocatore di evitare tutti i proiettili stando in un'area davvero grande dello schermo, che rimarrà completamente sicura per tutta la durata dell'attacco.
</p>
			<h3 xml:lang="en">Fan-games</h3>
			<p>Essendo la licenza di Touhou molto permissiva riguardo il riutilizzo delle tematiche del gioco, sono stati sviluppati <strong>moltissimi giochi non ufficiali riguardanti Touhou</strong>, di quasi tutti i generi, compresi <span xml:lang="en">action</span>, giochi di corse, giochi di carte, picchiaduro, <acronym xml:lang="en" title="Role Play Gaming">rpg</acronym> e <span xml:lang="en">puzzle</span>.<br/>
Uno dei più popolari è sicuramente <strong>Koumajou Densetsu <span xml:lang="en">Scarlet Symphony</span></strong>, reinterpretazione del gioco <em xml:lang="en">Castlevania</em> del 1986, anch'esso famoso per la sua difficoltà, che mantiene un <span xml:lang="en">gameplay</span> abbastanza fedele all'originale ma adatta ambientazioni e trama a quella di Touhou, con uno stile grafico particolarmente gotico. 
			</p>
